org  100h	; set location counter to 100h


START:

LEA DX, MSG1
MOV AH, 09H
INT 21H  

LEA SI, STR1
LEA DI, STR1  
MOV AH, 01H
NEXT:

INT 21H
CMP AL,0DH
JE TERMINATE
MOV [DI], AL
INC DI
JMP NEXT

TERMINATE:
 MOV AL,'$'
  MOV [DI],AL
DOTHIS:

DEC DI
MOV AL, [SI]
CMP [DI],AL
JNE NOTPALINDROME
INC SI 
CMP SI,DI
JL DOTHIS
PALINDROME:
MOV AH, 09H
LEA DX, MSG2
INT 21H
JMP END
NOTPALINDROME:
MOV AH,09H
LEA DX,MSG3
INT 21H
END:
MOV AH,4CH   
INT 21H


ret   ; return to the operating system.
    MSG1 DB 10,'ENTER THE STRING:$'
    MSG2 DB 10,'PAL:$'
    MSG3 DB 10,'NOT PAL:$' 
    STR1 DB 50 DUP(0)